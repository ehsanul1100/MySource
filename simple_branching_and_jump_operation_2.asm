



.STACK 100H

.DATA
    MGS1 DB 'ENTER SINGLE DIGIT (WITH OPTIONAL -SIGN): $'
    MGS2 DB 0DH,0AH, 'ABSOLUTE VALUE: $'
    NEWLINE DB 0DH,0AH, '$'


.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
    
    
    ;DISPLAY FIRST MESSAGE
    MOV AH,9
    MOV DX,OFFSET MGS1
    INT 21H 
    
    ;INPUT THE DIGIT
    MOV AH,1
    INT 21H
    
    ;CHECK IF THE FIRST CHARACTER IS MINUS SIGN OR NOT
    
    CMP AL,'-'
    JE NEGATIVE_INPUT
    
    ;POSITIVE INPUT 
    MOV BL,AL
    
    ;JUMP TO THE DISPLAY_RESULT 
    JMP DISPLAY_RESULT
    
NEGATIVE_INPUT:
    MOV AH,1
    INT 21H  
    MOV BL,AL
DISPLAY_RESULT:
    MOV AH,9
    MOV DX, OFFSET MGS2 
    INT 21H
    
    MOV AH,2
    MOV DL, BL
    INT 21H
    MOV AH,4CH
    INT 21H
MAIN ENDP
END MAIN
    
    
    